'''
学习使用 turtle 在屏幕上绘制图形
形状：五角星
@author kakakeven
'''
import turtle

turtle.pensize(3)
turtle.pencolor('red')
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.right(144)
turtle.forward(200)
turtle.mainloop()
